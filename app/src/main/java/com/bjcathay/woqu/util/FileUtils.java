    private static long fileSite(File baseDir) {
        long length = 0;
        File[] fs = baseDir.listFiles();
        for (int i = 0; i < fs.length; i++) {
            if (fs[i].isFile()) {
                System.out.println("file=" + fs[i].getAbsolutePath());
                length += fs[i].length();
            }
            if (fs[i].isDirectory()) {
                try {
                    length += fileSite(fs[i]);
                } catch (Exception e) {
                }
            }
        }
        return length;
    }

    public static void delete(File file) {
        if (file.isFile()) {
            file.delete();
            return;
        }

        if (file.isDirectory()) {
            File[] childFiles = file.listFiles();
            if (childFiles == null || childFiles.length == 0) {
                file.delete();
                return;
            }

            for (int i = 0; i < childFiles.length; i++) {
                delete(childFiles[i]);
            }
            file.delete();
        }
    }

